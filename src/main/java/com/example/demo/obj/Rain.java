package com.example.demo.obj;



public class Rain {
	
	int id;
	
	int value;
	

	public Rain() {}
	
	
	public Rain(int value) {
		this.value = value;
	}
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	public int getValue() {
		return value;
	}
	
	public void setValue(int value) {
		this.value = value;
	}

}

